    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFrame frame = new JFrame();
       
            album.setTracks(trackinfo.getTracks());
            if(trackinfo.getTracks().size()>0){
            try {
                if (controller.addAlbumsToDb(album)) {
                    JOptionPane.showMessageDialog(frame, "Album (  " + album.getTitle() + " ) Added ", "Message", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                }
                else
                    JOptionPane.showMessageDialog(frame, "Album cannot be Added !", "Alert", JOptionPane.ERROR_MESSAGE);      
            } catch (SQLException ex) {
                Logger.getLogger(AddAlbums.class.getName()).log(Level.SEVERE, null, ex);
            }
                }
                else {
                JOptionPane.showMessageDialog(frame, "Please add Songs!", "Alert", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFrame f = new JFrame();
        if (!title_txt.getText().isEmpty() && !priceTxt.getText().isEmpty()) {
            album.setTitle(title_txt.getText());
            try {
                album.setId(Integer.parseInt(id_txt.getText()));
                album.setReleaseYear(Integer.parseInt(getReleaseYear()));
                album.setQuantity(Integer.parseInt(quantityTxt.getValue().toString()));
                album.setGenre(genreTxt.getSelectedItem().toString());
                album.setRating(Integer.parseInt(albumRatingTxt.getValue().toString()));
                album.setPrice(Double.parseDouble(priceTxt.getText()));
                
                trackinfo.setVisible(true);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(f, ex.getMessage(),"Warning",JOptionPane.WARNING_MESSAGE);
            }
        } else {
                JOptionPane.showMessageDialog(f,"One or more fields are empty  !!" ,"Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

