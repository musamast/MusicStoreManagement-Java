    private String getReleaseYear() {
        Object value = rYearTxt.getValue();
        if (value instanceof Date) {
            Date date = (Date) value;
            SimpleDateFormat format = new SimpleDateFormat("yyy");
            String year = format.format(date);
            return year;
        }
        return "0000";
    }

    private void setGenres() {
        trackinfo = new TrackInfo();
        album = new Album();
        genreTxt.removeAllItems();
        for (String genre : album.getGenres()) {
            genreTxt.addItem(genre);
        }

    }

